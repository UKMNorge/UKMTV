<p class="lead">UKM er en åpen arena for alle ungdommer mellom 13 og 20 år, hvor man kan delta med akkurat det man vil, uansett erfaring!</p>

<p>Liker du å synge? Eller kanskje du liker å skrive bøker, montere lydforsterkere eller trikse med fotballer? Det at man kan delta med alt gjør at deltakerne har utrolig mange forskjellige interesser og type talenter.
</p>
<p>
UKM gir deg en fantastisk mulighet til å møte andre ungdommer som liker det samme, videreutvikle deg, dele erfaringer, få tilbakemeldinger, skape et nettverk og bli satt pris på for det du driver med! Det arrangeres lokalmønstringer i 360 kommuner og bydeler hvert år, og mellom 20 og 25 000 ungdommer deltar! Hørt om Karpe Diem? De bestemte seg for å danne rapgruppen for å melde seg på UKM! 
</p>
<p>UKM Norge lanserte i 2013 UKM-TV som en nett-tvkanal med mål om å vise frem noe av det som skjer på de ulike UKM-arenaene. Her finner du videoklipp fra både UKM-festivalen, fylkesmønstringer, noen lokalmønstringer samt andre godbiter.</p>

